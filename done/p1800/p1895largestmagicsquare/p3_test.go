func largestMagicSquare(grid [][]int) int {
	// Brute-force, annoying but works
	m, n := len(grid), len(grid[0])
	for k := min(m, n); k > 1; k-- {
		for rowOffset := 0; rowOffset+k <= m; rowOffset++ {
			for colOffset := 0; colOffset+k <= n; colOffset++ {
				var want int
				// Sum first row, this is what we want..
				for i := 0; i < k; i++ {
					want += grid[rowOffset][colOffset+i]
				}
				var got int
				// Check rows
				for i := 0; i < k; i++ {
					got = 0
					for j := 0; j < k; j++ {
						got += grid[rowOffset+i][colOffset+j]
					}
					if got != want {
						goto ContinueSearch
					}
				}
				// Check columns
				for i := 0; i < k; i++ {
					got = 0
					for j := 0; j < k; j++ {
						got += grid[rowOffset+j][colOffset+i]
					}
					if got != want {
						goto ContinueSearch
					}
				}
				// Check diagonal
				got = 0
				for i := 0; i < k; i++ {
					got += grid[rowOffset+i][colOffset+i]
				}
				if got != want {
					continue
				}

				// Check antidiagonal
				got = 0
				for i := 0; i < k; i++ {
					got += grid[rowOffset+k-i-1][colOffset+i]
				}
				if got != want {
					continue
				}

				// All OK return k
				return k
			ContinueSearch:
			}
		}
	}
	return 1
}

