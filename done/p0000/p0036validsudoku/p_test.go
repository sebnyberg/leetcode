func Test_isValidSudoku(t *testing.T) {
	for _, tc := range []struct {
		in   [][]byte
		want bool
	}{
		{
			[][]byte{
				{'8', '3', '.', '.', '7', '.', '.', '.', '.'},
				{'6', '.', '.', '1', '9', '5', '.', '.', '.'},
				{'.', '9', '8', '.', '.', '.', '.', '6', '.'},
				{'8', '.', '.', '.', '6', '.', '.', '.', '3'},
				{'4', '.', '.', '8', '.', '3', '.', '.', '1'},
				{'7', '.', '.', '.', '2', '.', '.', '.', '6'},
				{'.', '6', '.', '.', '.', '.', '2', '8', '.'},
				{'.', '.', '.', '4', '1', '9', '.', '.', '5'},
				{'.', '.', '.', '.', '8', '.', '.', '7', '9'},
			}, false,
		},
		{
			[][]byte{
				{'5', '3', '.', '.', '7', '.', '.', '.', '.'},
				{'6', '.', '.', '1', '9', '5', '.', '.', '.'},
				{'.', '9', '8', '.', '.', '.', '.', '6', '.'},
				{'8', '.', '.', '.', '6', '.', '.', '.', '3'},
				{'4', '.', '.', '8', '.', '3', '.', '.', '1'},
				{'7', '.', '.', '.', '2', '.', '.', '.', '6'},
				{'.', '6', '.', '.', '.', '.', '2', '8', '.'},
				{'.', '.', '.', '4', '1', '9', '.', '.', '5'},
				{'.', '.', '.', '.', '8', '.', '.', '7', '9'},
			}, true,
		},
		{
			[][]byte{
				{'7', '.', '.', '.', '4', '.', '.', '.', '.'},
				{'.', '.', '.', '8', '6', '5', '.', '.', '.'},
				{'.', '1', '.', '2', '.', '.', '.', '.', '.'},
				{'.', '.', '.', '.', '.', '9', '.', '.', '.'},
				{'.', '.', '.', '.', '5', '.', '5', '.', '.'},
				{'.', '.', '.', '.', '.', '.', '2', '.', '.'},
				{'.', '.', '.', '.', '.', '.', '.', '.', '.'},
				{'.', '.', '.', '.', '.', '.', '.', '.', '.'},
				{'.', '.', '.', '.', '.', '.', '.', '.', '.'},
			}, false,
		},
	} {
		t.Run(fmt.Sprintf("%+v", tc.in), func(t *testing.T) {
			require.Equal(t, tc.want, isValidSudoku(tc.in))
		})
	}
}

func isValidSudoku(board [][]byte) bool {
	var rows [9]uint16
	var cols [9]uint16
	var boxes [9]uint16
	for row := range board {
		for col := range board[row] {
			val := int(board[row][col] - '0')
			boxIdx := 3*(row/3) + col/3
			var bit uint16 = 1 << val
			if rows[row]&bit > 0 ||
				cols[col]&bit > 0 ||
				boxes[boxIdx]&bit > 0 {
				return false
			}
			rows[row] |= bit
			cols[col] |= bit
			boxes[boxIdx] |= bit
		}
	}
	return true
}
